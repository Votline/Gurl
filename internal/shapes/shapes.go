package shapes

func QuadUV() []float32 {
    return []float32{
        // x, y, z,   u, v
        -1,  1, 0,    0, 0,  // левый верхний
        -1, -1, 0,    0, 1,  // левый нижний
         1, -1, 0,    1, 1,  // правый нижний
        
        -1,  1, 0,    0, 0,  // левый верхний
         1, -1, 0,    1, 1,  // правый нижний
         1,  1, 0,    1, 0,  // правый верхний
    }
}
